    public static BbbModel GetModel(string algorithm, string fingerprint)
    {
        if (!algorithm.TryParseBbbAlgorithm(out BbbAlgorithm algo))
            return null;

        if (!fingerprint.TryParseBbbFingerprint(out BbbFingerprint fp))
            return null;

        return new BbbModel
        {
            Algorithm = algo,
            Fingerprint = fp,
            NegativeCurve = BbbCharts[((int)algo * BbbFingerprintExtensions.All.Length + (int)fp) * 2],
            PositiveCurve = BbbCharts[((int)algo * BbbFingerprintExtensions.All.Length + (int)fp) * 2 + 1],
            Threshold = fp == BbbFingerprint.MACCS ? 0.02 : 0,
        };
    }

    public static IEnumerable<BbbModel> GetModels()
    {
        foreach (BbbAlgorithm algo in BbbAlgorithmExtensions.All)
        {
            foreach (BbbFingerprint fp in BbbFingerprintExtensions.All)
            {
                yield return new BbbModel
                {
                    Algorithm = algo,
                    Fingerprint = fp,
                    NegativeCurve = BbbCharts[((int)algo * BbbFingerprintExtensions.All.Length + (int)fp) * 2],
                    PositiveCurve = BbbCharts[((int)algo * BbbFingerprintExtensions.All.Length + (int)fp) * 2 + 1],
                    Threshold = fp == BbbFingerprint.MACCS ? 0.02 : 0,
                };
            }
        }
    }

